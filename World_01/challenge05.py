# Write a program that reads an integer and displays
# its successor and predecessor on the screen

firstNumber = int(input('Enter an integer:'))
firstSucessor = firstNumber + 1
firstPredecessor = firstNumber - 1
print(f'The number you entered is {firstNumber}\n \
Sucessor is {firstSucessor}\n Prececessor is {firstPredecessor}')
